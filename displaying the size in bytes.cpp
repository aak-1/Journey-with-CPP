#include <iostream>
using namespace std;
int main(){
    cout<<"the size of the int variable is "<<sizeof(int)<<" bytes"<<endl;
    cout<<"the size of the float variable is "<<sizeof(float)<<" bytes"<<endl;
    cout<<"the size of the char variable is "<<sizeof(char)<<" bytes"<<endl;
    cout<<"the size of the short int variable is "<<sizeof(short int)<<" bytes"<<endl;
    cout<<"the size of the long int variable is "<<sizeof(long int)<<" bytes"<<endl;
    cout<<"the size of the double variable is "<<sizeof(double)<<" bytes"<<endl;
    cout<<"the size of the long double variable is "<<sizeof(long double)<<" bytes"<<endl;
    return 0;

}




